package net.room.db;

public class RoomBean {
	
	private int room_num;
	private int home_num;
	private int re_room;
	private int min_people;
	private int max_people;
	
	
	public int getRoom_num() {
		return room_num;
	}
	public void setRoom_num(int room_num) {
		this.room_num = room_num;
	}
	public int getHome_num() {
		return home_num;
	}
	public void setHome_num(int home_num) {
		this.home_num = home_num;
	}
	public int getRe_room() {
		return re_room;
	}
	public void setRe_room(int re_room) {
		this.re_room = re_room;
	}
	public int getMin_people() {
		return min_people;
	}
	public void setMin_people(int min_people) {
		this.min_people = min_people;
	}
	public int getMax_people() {
		return max_people;
	}
	public void setMax_people(int max_people) {
		this.max_people = max_people;
	}
	
	

}
